fpmath_t myasin(fpmath_t v)  			{ return __myasin(v); }
fpmath_t myatan(fpmath_t v)  			{ return __myatan(v); }
fpmath_t mysqrt(fpmath_t v)  			{ return __mysqrt(v); }
fpmath_t mypow(fpmath_t v, fpmath_t e)  { return __mypow(v, e); }
